<strong>Erste Jahre</strong><br/>
Gemeinsam mit Vater Franz und unterstützt von Bruder Philipp wurden Golf-Eventkonzepte ausgearbeitete und Sponsoren für diese gefunden. 2001 wurde bereits eine Bürokraft eingestellt, um Kosten zu sparen in einem spartanischen Büro auf der Golftrainingsanlage, welche Jochen  gepachtet hatte.<br/>
Erfolge stellten sich bald ein und Eventserien wie die „PFS-Matchplay League“, „Miele Ladies Masters“, „Caribbean Golf Challenge presented by 3“ und die B2B-Plattform „Business Golf League“ halfen dem Unternehmen dabei zu expandieren, bald in eine eigene Immobilie in der Ziegeleistr. 32.<br/>
Die Nachfrage nach Werbemitteln seitens der Auftraggeber für Golfveranstaltungen war so stark, dass Jochen und Philipp 2007 beschlossen nach China zu reisen, um direkt mit Herstellern von Werbemitteln und Golf-Accessoires zu arbeiten. Bald entwickelte sich der Warenimport und Handel zu einem eigenen Geschäftsfeld. <br/>
2008 wurde die Marke PELT gegründet, nachdem die Brüder einen Gürtel aus Kunststoff entwickelt hatten, welche bis heute den Standard in diesem Marktsegment setzt. Rund um PELT entstand über die Jahre eine Kollektion aus Lifestyle-Accessoires welche über ein breites Vertriebsnetz in den Handel wie auch an B2B-Kunden gelangt.<br/>
<strong>Heute</strong><br/>
Mit dem Wandel der Eventbranche und immer restriktiveren Verhaltensrichtlinien in Unternehmen weltweit ist heute die Beschaffung und der Handel mit Prämienartikeln das Kerngeschäft der S.E.A. <br/>
Mit der Gründung der MRSOURCING GmbH. wurde dieser Entwicklung Rechnung getragen. <br/>
Die 2005 erstmals ausgetragene BUSINESS GOLF LEAGUE ist das einzige Eventprodukt, welches S.E.A. in den letzten 15 Jahren ohne Unterbrechung ausgerichtet hat. Bei der Konzeptionierung von B2B-Events wie der BGL und Incentive Veranstaltungen sind die 20-Jahre Erfahrung des Unternehmens heute wertvoller denn je.<br/>
<strong>Ausblick</strong><br/>
Die Gestaltung von B2B Webportalen und deren Nutzung zur Bindung von Kunden mit dem Bedarf zur Lagerung von Handelsware bzw. Prämienartikeln sowie der Aufbau der MRSOURCING GmbH als Eigenständiges Unternehmen, werden die kommenden Jahre unseres Unternehmens prägen. Eingeführte Produkte wie die „Business Golf League“, welche seit über 15 Jahren eine hervorragende Plattform zur Vernetzung von Unternehmen untereinander darstellt werden weiter ausgebaut, ebenso wie das Distributionsnetz der Marke PELT. Dabei wird der „Upcycling-Gedanke“ wird noch stärker im Fokus stehen und das Profil von PELT weiter geschärft werden. Besonderen Wert legen wir dabei auf die Nutzung von Synergien mit Produkten wie „VegasGolf.DE“ insbesondere in Bezug auf den Ausbau der Kundenstruktur im Golfsport. Ein weiterer Schwerpunkt wird auf die Entwicklung von PELT-OEM Kollektionen gemeinsam mit unseren Key-Account Kunden bei Geschenkartikeln und Prämien liegen.<br/>
